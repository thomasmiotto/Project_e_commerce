    <h1>Politique de confidentialité</h1>

    <h2>1. Introduction</h2>
    <p>Cette politique de confidentialité explique comment nous utilisons les informations que nous collectons lorsque vous utilisez notre site Web. Veuillez lire attentivement cette politique de confidentialité avant d'utiliser notre site Web ou de fournir des informations personnelles. En utilisant notre site Web, vous acceptez les pratiques décrites dans cette politique de confidentialité.</p>

    <h2>2. Les informations que nous collectons</h2>
    <p>Nous collectons les informations suivantes lorsque vous utilisez notre site Web :</p>
    <ul>
        <li>Informations personnelles telles que votre nom, votre adresse e-mail, votre numéro de téléphone, etc.</li>
        <li>Informations non personnelles telles que votre adresse IP, le type de navigateur, les pages visitées, etc.</li>
    </ul>

    <h2>3. Comment nous utilisons vos informations</h2>
    <p>Nous utilisons les informations que nous collectons pour :</p>
    <ul>
        <li>Fournir, exploiter et améliorer notre site Web.</li>
        <li>Personnaliser votre expérience et vous fournir du contenu et des publicités ciblés.</li>
        <li>Répondre à vos demandes et vous fournir un support client.</li>
        <li>Communiquer avec vous concernant des mises à jour, des alertes de sécurité ou des offres spéciales.</li>
    </ul>

    <h2>4. Divulgation des informations</h2>
    <p>Nous ne partageons pas vos informations personnelles avec des tiers, sauf dans les cas suivants :</p>
    <ul>
        <li>Si nous obtenons votre consentement explicite.</li>
        <li>Pour répondre à une demande légale ou se conformer à une obligation légale.</li>
        <li>Pour protéger nos droits, notre propriété ou notre sécurité, ainsi que ceux de nos utilisateurs ou du public.</li>
    </ul>

    <h2>5. Vos droits</h2>
    <p>Vous avez certains droits concernant vos informations personnelles, notamment :</p>
    <ul>
        <li>Le droit d'accéder, de corriger, de mettre à jour ou de supprimer vos informations personnelles.</li>
        <li>Le droit de vous opposer au traitement de vos informations personnelles.</li>
        <li>Le droit de demander la limitation du traitement de vos informations personnelles.</li>
        <li>Le droit de retirer votre consentement.</li>
    </ul>

    <h2>6. Modifications de cette politique de confidentialité</h2>
    <p>Nous nous réservons le droit de modifier cette politique de confidentialité à tout moment. Toutes les modifications seront publiées sur cette page.</p>

